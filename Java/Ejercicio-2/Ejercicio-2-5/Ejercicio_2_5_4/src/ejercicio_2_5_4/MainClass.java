package ejercicio_2_5_4;

/**
 *Clase Main con un blucle do while para imprimir los 10 primeros numeros
 * @author Oscar Vaz Falagan
 */
public class MainClass {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int numero=0;
        //Bucle do while para los 10 primeros numeros
        do{
            System.out.println(numero);
            numero=numero+1;
            
        }while(numero<10);
        
    }
        
}
    

